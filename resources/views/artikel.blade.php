        <h1>Framework</h1>
        <p>Framework Secara umum, framework adalah struktur nyata atau konseptual yang dimaksudkan berfungsi sebagai pendukung atau panduan membangun dan memperluas struktur menjadi sesuatu yang bermanfaat.
        <p>Dalam sistem komputer, istilah framework memiliki makna yang sedikit berbeda Apa itu framework dalam dunia pemrograman?
        <p>Ketika mengacu pada software komputer, pengertian framework adalah fondasi dengan tingkat kerumitan yang ditentukan dan dapat diperluas oleh pemrogram dengan menggunakan kode mereka sendiri termasuk kompiler, juru bahasa, atau API. Secara umum, framework menyediakan lingkungan yang memfasilitasi jenis pemrograman tertentu untuk proyek pengembangan software.
        <p>Sebuah framework dalam pemrograman seringkali merupakan struktur berlapis yang menunjukkan program apa yang dapat atau harus dibangun dan bagaimana framework akan saling terkait.
